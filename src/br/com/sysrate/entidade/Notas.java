package br.com.sysrate.entidade;

/**
 *
 * @author migue
 */
public class Notas {
    private int notasID;
    private int professorID;
    private int didatica;
    private int qualidadeMaterial;
    private int qualidadeCorrecao;
    private int receptividade;
    private int respeito;

    /**
     * @return the notasID
     */
    public int getNotasID() {
        return notasID;
    }

    /**
     * @param notasID the notasID to set
     */
    public void setNotasID(int notasID) {
        this.notasID = notasID;
    }

    /**
     * @return the professorID
     */
    public int getProfessorID() {
        return professorID;
    }

    /**
     * @param professorID the professorID to set
     */
    public void setProfessorID(int professorID) {
        this.professorID = professorID;
    }

    /**
     * @return the didatica
     */
    public int getDidatica() {
        return didatica;
    }

    /**
     * @param didatica the didatica to set
     */
    public void setDidatica(int didatica) {
        this.didatica = didatica;
    }

    /**
     * @return the qualidadeMaterial
     */
    public int getQualidadeMaterial() {
        return qualidadeMaterial;
    }

    /**
     * @param qualidadeMaterial the qualidadeMaterial to set
     */
    public void setQualidadeMaterial(int qualidadeMaterial) {
        this.qualidadeMaterial = qualidadeMaterial;
    }

    /**
     * @return the qualidadeCorrecao
     */
    public int getQualidadeCorrecao() {
        return qualidadeCorrecao;
    }

    /**
     * @param qualidadeCorrecao the qualidadeCorrecao to set
     */
    public void setQualidadeCorrecao(int qualidadeCorrecao) {
        this.qualidadeCorrecao = qualidadeCorrecao;
    }

    /**
     * @return the receptividade
     */
    public int getReceptividade() {
        return receptividade;
    }

    /**
     * @param receptividade the receptividade to set
     */
    public void setReceptividade(int receptividade) {
        this.receptividade = receptividade;
    }

    /**
     * @return the respeito
     */
    public int getRespeito() {
        return respeito;
    }

    /**
     * @param respeito the respeito to set
     */
    public void setRespeito(int respeito) {
        this.respeito = respeito;
    }
    
}
